namespace Developer.CSharp.Fundamentals
{
    public class Operators
    {
        /*  ****** ARITHMETIC OPERATORS ******
        *  
        *  Following table shows all the arithmetic operators supported by C#. Assume variable A holds 10 and variable B holds 20 then:
        *  
        *  OPERATOR        EXAMPLE         DESCRIPTION
        *  +	            A + B = 30      Adds two operands
        *  -	            A - B = -10     Subtracts second operand from the first
        *	*               A * B = 200     Multiplies both operands
        *  /	            B / A = 2       Divides numerator by de-numerator
        *  %	            B % A = 0       Modulus Operator and remainder of after an integer division
        *  ++	            A++ = 11        Increment operator increases integer value by one
        *  --	            A-- = 9         Decrement operator decreases integer value by one
        */


        /*  ****** RELATIONAL OPERATORS *******
         *  
         *  Following table shows all the relational operators supported by C#. Assume variable A holds 10 and variable B holds 20, then:
         *   
         *  OPERATOR	EXAMPLE                 DESCRIPTION
         *  ==	        (A == B) is not true.   Checks if the values of two operands are equal or not, if yes then condition becomes true.
         *  !=	        (A != B) is true.       Checks if the values of two operands are equal or not, if values are not equal then condition becomes true.
         *  >	        (A > B) is not true.    Checks if the value of left operand is greater than the value of right operand, if yes then condition becomes true.
         *  <	        (A < B) is true.        Checks if the value of left operand is less than the value of right operand, if yes then condition becomes true.
         *  >=	        (A >= B) is not true.   Checks if the value of left operand is greater than or equal to the value of right operand, if yes then condition becomes true.
         *  <=	        (A <= B) is true.       Checks if the value of left operand is less than or equal to the value of right operand, if yes then condition becomes true.
         */


        /*  ****** LOGICAL OPERATORS ******
         *  Following table shows all the logical operators supported by C#. 
         *  Assume variable A holds Boolean value true and variable B holds Boolean value false, then:
         *  OPERATOR    EXAMPLE                     DESCRIPTION
         *  &&	        (A && B) is false.          Called Logical AND operator. If both the operands are non zero then condition becomes true.
         *  ||	        (A || B) is true.           Called Logical OR Operator. If any of the two operands is non zero then condition becomes true.
         *  !	        !(A && B) is true.          Called Logical NOT Operator. Use to reverses the logical state of its operand. If a condition is true then Logical NOT operator will make false.
         */


        /*  ****** ASSIGNMENT OPERATORS ******
         *  
         *  There are following assignment operators supported by C#
         *  
         *  OPERATOR    EXAMPLE                                     DESCRIPTION
         *  =	        C = A + B assigns value of A + B into C     Simple assignment operator, Assigns values from right side operands to left side operand
         *  +=	        C += A is equivalent to C = C + A           Add AND assignment operator, It adds right operand to the left operand and assign the result to left operand
         *  -=	        C -= A is equivalent to C = C - A           Subtract AND assignment operator, It subtracts right operand from the left operand and assign the result to left operand
         *  *=	        C *= A is equivalent to C = C * A           Multiply AND assignment operator, It multiplies right operand with the left operand and assign the result to left operand
         *  /=	        C /= A is equivalent to C = C / A           Divide AND assignment operator, It divides left operand with the right operand and assign the result to left operand
         *  %=	        C %= A is equivalent to C = C % A           Modulus AND assignment operator, It takes modulus using two operands and assign the result to left operand
         */

        // MORE INFORMATION ABOUT OPERATORS: https://www.tutorialspoint.com/csharp/csharp_operators.htm
    }
}